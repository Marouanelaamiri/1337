int	ft_isalpha(int c)
{
	{
		if ((c >  'z' || c <  'a') && (c  > 'Z' || c < 'A'))
		{
			return 0;
		}
	}
	return 1;
}
#include <stdio.h>
int main()
{
	int alpha= '1';
	printf("%d\n", ft_isalpha(alpha));
}
