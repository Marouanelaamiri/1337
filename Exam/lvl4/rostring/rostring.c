#include <unistd.h>

int main(int ac, char **av)
{
	int i;
	int j;
	int k;

	if (ac > 1)
	{
		i = 0;
		j = 0;
		while (av[1][i] == ' ' || av[1][i] == '\t' )
			i++;
		k = i;
		while (av[1][i] != ' ' && av[1][i] != '\t' && av[1][i] != '\0')
			i++;
		while(av[1][i])
		{
			if ((av[1][i] && av[1][i] != ' ' && av[1][i] != '\t') && (av[1][i - 1] == ' ' || av[1][i - 1] == '\t'))
			{
				while (av[1][i] && av[1][i] != ' ' && av[1][i] != '\t')
					write(1, &av[1][i++],1);
				write (1, " ",1);
			}
			i++;
		}
		while (av[1][k] && av[1][k] != ' ' && av[1][k] != '\t' )
			write (1, &av[1][k++], 1);
	}
	write (1, "\n",1);
	return 0;
}	
